<template>
  <div class="zchPopover">
    <div ref="popoverContent" class="zchPopover-popoverWrapper" :class="`zchPopover-popoverWrapper-${position}`" v-if="popover">
      <div class="zchPopover-popover" :class="`zchPopover-popover-${position}-arrow`">
        <div class="zchPopover-popover-title" v-show="$slots.title">
          <slot name="title"></slot>
        </div>
        <div class="zchPopover-popover-content">
          <slot name="content"></slot>
        </div>
      </div>
    </div>
    <div ref="userContent" class="userContentWrapper">
      <slot></slot>
    </div>
  </div>
</template>

<script>
export default {
  name: 'ZchPopover',
  props: {
    position: {
      type: String,
      default: 'top',
      validator (value) {
        return ['top', 'left', 'bottom', 'right'].indexOf(value) >= 0
      }
    },
    trigger: {
      type: String,
      default: 'click',
      validator (value) {
        return ['click', 'hover'].indexOf(value) >= 0
      }
    }
  },
  data () {
    return {
      popover: false,
    }
  },
  mounted () {
    if (this.trigger === 'click') {
      this.$refs.userContent.addEventListener('click', this.openPoppver)
    } else {
      this.$refs.userContent.addEventListener('mouseenter', this.openPoppver)
      this.$refs.userContent.addEventListener('mouseleave', this.closePoppver)
    }
  },
  destroyed () {
    if (this.trigger === 'click') {
      this.$refs.userContent.removeEventListener('click', this.openPoppver)
    } else {
      this.$refs.userContent.removeEventListener('mouseenter', this.openPoppver)
      this.$refs.popoverContent.removeEventListener('mouseenter', this.openPoppver)
      this.$refs.userContent.removeEventListener('mouseleave', this.closePoppver)
      this.$refs.popoverContent.removeEventListener('mouseleave', this.closePoppver)
    }
  },
  methods: {
    openPoppver () {
      this.popover = true
      this.$nextTick(() => {
        document.body.appendChild(this.$refs.popoverContent)
        this.setPopoverPosition()
        this.trigger === 'click' && document.addEventListener('click', this.clickDocument)
      })
    },
    closePoppver () {
      if (this.trigger === 'click') {
        this.popover = false
        document.removeEventListener('click', this.clickDocument)
      } else {
        setTimeout(() => {
          this.popover = false
        }, 500)
      }
    },
    clickDocument (e) {
      if (this.$refs.userContent.contains(e.target)) return
      if (this.$refs.popoverContent.contains(e.target)) return
      this.closePoppver()
    },
    setPopoverPosition () {
      const { left, top, width, height } = this.$refs.userContent.getBoundingClientRect()
      const { width: popoverContentWidth, height: popoverContentHeight } = this.$refs.popoverContent.getBoundingClientRect()
      const map = {
        top: { top: top + scrollY, left: left + scrollX - (popoverContentWidth - width) / 2 },
        left: { top: top + scrollY - (popoverContentHeight - height) / 2, left: left + scrollX },
        bottom: { top:top + scrollY + height, left: left + scrollX - (popoverContentWidth - width) / 2 },
        right: { top: top + scrollY - (popoverContentHeight - height) / 2, left: left + scrollX + width },
      }
      const position = map[this.position]
      this.$refs.popoverContent.style.top = position.top + 'px'
      this.$refs.popoverContent.style.left = position.left + 'px'
    },
    handleClick () {
      if (this.popover) {
        this.closePoppver()
      } else {
        this.openPoppver()
      }
    }
  }
}
</script>


